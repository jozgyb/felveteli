  <h3><i>A központi írásbeli felvételi vizsgára készülni kell</i></h3>
Bár már a felvételi vizsga-előkészítők zömének indulásáról lemaradtunk, de azért otthon tud készülni a gyerek az írásbeli vizsgára egyedül is. És készülni is kell, ugyanis ez nem annyi lesz, mint egy sima iskolai dolgozat: típusfeladatokkal és rövid idővel fog szembesülni a vizsgán a gyerek. Szülőként nehéz a tananyagban nyolcadikban segíteni, hiszen már elfelejtettük a felét (főleg matekból), de abban támogathatjuk a gyereket, hogy mérjük neki az időt és kinyomtatjuk az oktatas.hu honlapról az előző évek központi feladatsorait. Ugyanitt megtalálhatók a megoldási kulcsok is a feladatokhoz, így aztán ellenőrizni is tudjuk majd szépen a gyerek próbavizsgáját otthon. Próbáljunk neki vizsgakörülményeket létrehozni, hogy szokja az érzést, hogy rövid idő alatt sok feladatot kell megoldania.
